package P_Board.Util;

/**
 * Cw 클래스 - 화면 출력을 담당하는 유틸리티 클래스입니다.
 */
public class Cw {
	/**
	 * 문자열을 출력하는 메서드
	 * 
	 * @param s 출력할 문자열
	 */
	public static void w(String s) {
		System.out.print(s);
	}

	/**
	 * 문자열을 출력하고 개행하는 메서드
	 * 
	 * @param s 출력할 문자열
	 */
	public static void wn(String s) {
		System.out.println(s);
	}
}